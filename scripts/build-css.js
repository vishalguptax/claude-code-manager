#!/usr/bin/env node
/**
 * Build step: concatenate all webview CSS files into dist/webview/styles.css.
 *
 * Order matters — base.css defines CSS variables that later files reference,
 * components.css ships shared primitives (buttons, tabs, menus), and the
 * feature-specific files override as needed.
 */

const fs = require("fs");
const path = require("path");

const ROOT = path.resolve(__dirname, "..");
const SRC = path.join(ROOT, "src", "styles");
const OUT_DIR = path.join(ROOT, "dist", "webview");
const OUT_FILE = path.join(OUT_DIR, "styles.css");

/**
 * Files in load order. Keep this list explicit so CSS cascade is predictable
 * — don't use readdir(), since order would depend on filesystem.
 */
const FILES = [
  "base.css",
  "components.css",
  "sessions.css",
  "skills.css",
  "features.css",
  "mcp.css",
  "agents.css",
  "account.css",
];

function build() {
  const parts = FILES.map((name) => {
    const file = path.join(SRC, name);
    const content = fs.readFileSync(file, "utf-8");
    return `/* ── ${name} ── */\n${content}`;
  });

  const banner = "/* Generated by scripts/build-css.js — do not edit */\n";
  const output = banner + parts.join("\n\n");

  fs.mkdirSync(OUT_DIR, { recursive: true });
  fs.writeFileSync(OUT_FILE, output);

  const sizeKb = (output.length / 1024).toFixed(1);
  console.log(`build-css: wrote ${path.relative(ROOT, OUT_FILE)} (${sizeKb} KB, ${FILES.length} files)`);
}

build();
